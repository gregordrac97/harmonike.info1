	<h3 align="center">HARMONIKE ZUPAN</h3>
	<p>Hiša harmonike ZUPAN vključuje razvoj, proizvodnjo, prodajo in servis harmonik, glasbeno galerijo s 350 ročno izdelanimi lesenimi intarzijami, pojočimi stopnicami in slikami z glasbenimi motivi, muzej starih harmonik in arhiv dokumentov o najstarejših harmonikah na Slovenskem.</p>
	
	<h3 align="center">VALENTIN ZUPAN</h3>
	<p>Po poklicu izdelovalec lesenih glasbenih instrumentov. Svojo kariero neslutenih razsežnosti je začel leta 1951 kot učenec v tovarni glasbil Mengeš. Prvo harmoniko je za šolsko razstavo izdelal pri rosnih 16.letih. Po 29. Letih dela v tovarni se je odločil, da se bo popolnoma posvetil razvoju, izboljševanju in izdelovanju povsem lastnih modelov glasbil.<br>Zaradi nenaklonjenosti takratnih političnih oblasti, je leta 1980 lastno obrt preselil v sosednjo Italijo, kjer je lahko 18. let nemoteno nadaljeval svoje poslanstvo. Valentin Zupan je eden redkih proizvajalcev harmonik, ki po svojih zamislih sam nariše načrte, naredi maketo in se potem loti izdelave vzorca. Vse, kar je v svojem življenju ustvaril, je plod njegovih zamisli in rok. V osmih desetletjih svojega življenja njegovo delo še vedno dosega vrhunce. Harmonike razvija in izdeluje v domačem Mengšu. Zupanove harmonike so si zaradi svoje kakovosti in izvirnosti pridobile velik ugled in dobro ime tako doma kot po celem svetu.</p>
	
